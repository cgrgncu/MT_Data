% 也沒有繪圖的需求，這裡就先不探究。此項是必要資料，從前一個統計運算步驟得到的。
% 
% 同理，「>ZXYR」、「>ZXYI」分別為阻抗張量的Zxy元素的實部與虛部，這兩項組合成一
% 個複數單位為[(mV/km)/nT]。此項是必要資料，從前一個統計運算步驟得到的。
% 同理，「>ZYXR」、「>ZYXI」分別為阻抗張量的Zyx元素的實部與虛部，這兩項組合成一
