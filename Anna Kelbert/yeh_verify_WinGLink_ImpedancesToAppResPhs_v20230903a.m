% 說明:
% WinGLink輸出的EDI檔案我稱之為「ImpedanceTipperEdi」，是有複數的阻抗張量(Impedance)
% 及其VAR，也有複數的傾子(Tipper)。根據「Kelbert, A. (2020). EMTF XML: New data 
% interchange format and conversion tools for electromagnetic transfer functions. 
% Geophysics, 85(1), F1-F17.」指出，這種EDI檔案因為不儲存逆訊號功率矩陣(inverse signal power matrix)
% 與殘差值的共變異數矩陣(residual covariance matrix)，因此VAR項目在進行旋轉後就
% 變得不太正確。因為目前三維逆推都要求所有測站在同樣地座標系，常見的慣例就是將測站
% 旋轉至X朝北Y朝東，以方位角標註為0，也就是ROT=0，所以採用「ImpedanceTipperEdi」
% 來儲存檔案的時候，強烈建議保存ROT=0的檔案。這也是Kelbert, A. (2020).目前選擇的
% 策略。
% 首先介紹基本必須存在的重要項目，這些項目是來自於線性回歸的統計運算，最終儲存下
% 來的內容，但EDI檔案為了解省空間並沒有很好的標註單位，這裡將參考Kelbert, A. (2020)
% 整理的單位在這裡重新說明一次:
% 「>FREQ」為各項資料的代表頻率(頻譜分析通常要選中心頻率及頻寬範圍)，單位:[Hz]。
%
% 「>ZROT」為阻抗張量的旋轉角度，與方位角(Azimuth)相同描述，正北=0度，正東=90度
% ，單位:[度]。在現在的儲存策略中，這項應該全都是0，表示所有頻率都朝正北。此項是
% 必要資料，從前一個統計運算步驟得到的。
%
% 「>ZXXR」、「>ZXXI」分別為阻抗張量的Zxx元素的實部與虛部，這兩項組合成一個複數
% 單位為[(mV/km)/nT]。硬要說的話，實部、虛部與複數都是相同單位。此項是必要資料，
% 從前一個統計運算步驟得到的。
